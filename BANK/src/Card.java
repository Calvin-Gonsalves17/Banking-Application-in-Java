
public class Card {
	String cardnumber;
	String cardname;
	String expiry;
	String cvv;
	String phonenumber;
	double cardbalance;
	
	Card(String cardnumber,String cardname,String expiry,String cvv,String phonenumber,double cardbalance){
		this.cardnumber=cardnumber;
		this.cardname=cardname;
		this.expiry=expiry;
		this.cvv=cvv;
		this.phonenumber=phonenumber;
		this.cardbalance=cardbalance;
	}

}
